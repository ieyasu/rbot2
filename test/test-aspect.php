<?
	require_once("test-framework.php");
	testMod("aspect");
	assertReply("", "Return the aspect ratio for 2 numbers, e.g. 16:9, 4:3, etc.");
	assertReply("1 1", "1:1 is 1:1");
	assertReply("1:1", "1:1 is 1:1");
	assertReply("1000:1000", "1000:1000 is 1:1");
	assertReply("1000 1000", "1000:1000 is 1:1");
	assertReply("1 2", "1:2 is 1:2");
	assertReply("16 9", "16:9 is 16:9");
	assertReply("16 10", "16:10 is 16:10 (8:5)");
	assertReply("10 16", "10:16 is 10:16 (5:8)");
	assertReply("4 3", "4:3 is 4:3");
	assertReply("4 3", "4:3 is 4:3");
	assertReply("1024 768", "1024:768 is 4:3");
	assertReply("1024x768", "1024:768 is 4:3");
	assertReply("768x1024", "768:1024 is 3:4");
	assertReply("1.85:1", "1.85:1 is 1.85:1 (37:20)");
	assertReply("1:1.85", "1:1.85 is 1:1.85 (20:37)");
	assertReply("2.39:1", "2.39:1 is 2.39:1 (239:100)");
	assertReply("1:2.39", "1:2.39 is 1:2.39 (100:239)");
	assertReply("239:241", "239:241 is 239:241");
	assertReply("-1:1", "Please use only positive numbers");
	assertReply("0:1", "Please use only positive numbers");
	assertReply("1:0", "Please use only positive numbers");
	assertReply("1:-1", "Please use only positive numbers");
	assertReply("1:1:1", "Please provide only 2 numbers");
	assertReply("1:1:1:1", "Please provide only 2 numbers");
	assertReply("1", "Please provide 2 numbers");
	assertReply("asdf", "Please provide 2 numbers");
	assertReply("asdf 1:1", "1:1 is 1:1");
	assertReply("asdf 1:1 asdf", "1:1 is 1:1");
	assertReply("asdf 1 asdf : asdf 1 asdf", "1:1 is 1:1");
	assertReply("asdf 1 asdf : asdf 1 asdf 1", "Please provide only 2 numbers");
	assertReply("3 1:1", "Please provide only 2 numbers");
	assertReply("-asdf 1:1", "1:1 is 1:1");
	assertReply("- 1:1", "1:1 is 1:1");
	assertReply("asdf - 1:1", "1:1 is 1:1");
	assertReply("asdf- 1:1", "1:1 is 1:1");
	assertReply("1:1 asdf-asdf", "1:1 is 1:1");
	assertReply("1:1 asdf-", "1:1 is 1:1");
?>
